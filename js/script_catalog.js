var makeElement = (tagName, className, text, id) => {
    var element = document.createElement(tagName);
    element.classList.add(className);
    if (id) {
        element.id = id;
    }
    if (text) {
        element.textContent = text;
    }
    return element;
};

var createCard = (product) => {
var listItem = makeElement('li', 'cakes-card');
var picture = makeElement('img');
picture.src = product.imgUrl;
picture.alt = product.title;
listItem.appendChild(picture);

var title = makeElement('h4', 'product-title', product.title);
listItem.appendChild(title);

var weight_text = makeElement('p', 'info');
var weight_title = makeElement('p', 'weight', 'Вес:');
var weight = makeElement('p', 'weight-info', product.weight);
weight_text.appendChild(weight_title);
weight_text.appendChild(weight);
listItem.appendChild(weight_text);

var calories_text = makeElement('p', 'info');
var calories_title = makeElement('p', 'calories', 'Кк на 100г:');
var calories = makeElement('p', 'calories-info', product.calories);
calories_text.appendChild(calories_title);
calories_text.appendChild(calories);
listItem.appendChild(calories_text);

var price_text = makeElement('p', 'info');
var price = makeElement('p', 'cake-price', product.price);
var button_struc = makeElement('button', 'button-struc', 'Состав');
price_text.appendChild(price);
price_text.appendChild(button_struc);
listItem.appendChild(price_text);

var compositionGroup = makeElement('p', 'composition');
var compositionText = makeElement('p', 'composition-text', product.composition);
var compositionArrow = makeElement('p', 'composition-arrow', 'мяу');
compositionGroup.appendChild(compositionText);
compositionGroup.appendChild(compositionArrow);
listItem.appendChild(compositionGroup);
compositionGroup.style.display = 'none';
listItem.appendChild(compositionGroup);

var button_catalog = makeElement('button', 'button-order', 'Сделать заказ', 'button-order');
listItem.appendChild(button_catalog);

button_struc.addEventListener('mouseover', function() {
compositionGroup.style.display = 'block';
});

button_struc.addEventListener('mouseout', function() {
compositionGroup.style.display = 'none';
});

return listItem;
};

